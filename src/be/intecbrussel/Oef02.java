package be.intecbrussel;

public class Oef02 {
    public static void main(String[] args) {

        int[][] nummers= new int[10][10];



        for (int row=0; row<nummers.length;row++){
            for (int col=0; col<nummers[row].length;col++){
                System.out.print(nummers[row][col] + " ");
            }
            System.out.println();
        }














    }
}
